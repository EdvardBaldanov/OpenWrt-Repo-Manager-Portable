#!/bin/bash
# Обертка для полного цикла обновления (Sync + Publish)
# Используется в Cron для регулярного обновления.

SCRIPT_DIR=$(cd "$(dirname "$0")" && pwd)

# 1. Запуск синхронизации (скачивание)
/bin/bash "$SCRIPT_DIR/repo_sync.sh"

# 2. Запуск публикации (сборка индексов)
# Мы запускаем публикацию всегда, чтобы убедиться, что индексы корректны,
# даже если обновлений не было (например, если файлы были удалены вручную).
/bin/bash "$SCRIPT_DIR/repo_publish.sh"